// Функция определения жанровых эмодзи по названию
function getGenreEmoji(title) {
  const t = title.toLowerCase();
  if (t.includes('кошмар') || t.includes('ужас') || t.includes('мертв') || t.includes('кровь')) return '🧟🩸🔪';
  if (t.includes('любовь') || t.includes('сердце') || t.includes('свадьба')) return '❤️💍🥂';
  if (t.includes('война') || t.includes('солдат') || t.includes('битв')) return '🪖🎖️🔫';
  if (t.includes('космос') || t.includes('звезд') || t.includes('планет')) return '🌌🛸👨‍🚀';
  if (t.includes('смех') || t.includes('комедия') || t.includes('приключ')) return '🎢😁🗺️';
  if (t.includes('детектив') || t.includes('убий') || t.includes('след')) return '🕵️‍♂️🕯️🔎';
  return '🎬🍿🎞️';
}

async function start() {
  console.log('Fetching all movies...');
  const { data: movies } = await supabase.from('kinokadr_movies').select('*');
  
  console.log(`Cleaning target table...`);
  await supabase.from('emojino_movies').delete().neq('id', '0');

  const mapped = movies.map(m => {
    const known = MOVIE_DB[m.title_ru];
    const isSerial = known?.t === 'serial' || m.title_ru.toLowerCase().includes('сериал') || m.title_ru.toLowerCase().includes('сезон');
    
    return {
      id: `m-${m.id}`,
      title_ru: m.title_ru,
      type: isSerial ? 'serial' : 'film',
      year: m.year,
      emoji: known?.e || getGenreEmoji(m.title_ru),
      hints: known?.h || [
        `Вышел в ${m.year} году`, 
        isSerial ? 'Это сериал, а не фильм' : 'Это полнометражное кино', 
        m.year > 2020 ? 'Современный проект' : 'Классика кино'
      ]
    };
  });

  const chunkSize = 50;
  for (let i = 0; i < mapped.length; i += chunkSize) {
    const chunk = mapped.slice(i, i + chunkSize);
    await supabase.from('emojino_movies').insert(chunk);
    console.log(`Inserted chunk ${i/chunkSize + 1}`);
  }

  console.log('SUCCESS: Base is now rich and unique!');
}

start();
